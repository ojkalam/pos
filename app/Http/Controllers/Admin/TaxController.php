    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
                'tax_name' => 'required',
                'tax_rate' => 'required',
            ]);

        $brand = new Tax;
        $brand->tax_name        = $request->tax_name;
        $brand->tax_rate        = $request->tax_rate;
        $brand->save();

        return redirect()->route('taxes.index')->with('successMsg','Tax Successfully Saved');
    }

